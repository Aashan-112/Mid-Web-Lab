


function Cup1() {
    let  cup1rem =  (2000-250)/1000 + "L";
    const rem = document.querySelector('.remLtr');
    rem.setAttribute('style', 'font-size: 30.5px');
    rem.innerHTML = cup1rem;
    let cup1res = 250/2000 * 100;
    const perc =  document.getElementById('percentage'); 
    perc.setAttribute('style', 'height: 12.5%');
    perc.innerHTML = cup1res + "%";
    const cupclr = document.getElementById('cup1');
    cupclr.setAttribute('style', 'background-color: #6ab3f8');
}
